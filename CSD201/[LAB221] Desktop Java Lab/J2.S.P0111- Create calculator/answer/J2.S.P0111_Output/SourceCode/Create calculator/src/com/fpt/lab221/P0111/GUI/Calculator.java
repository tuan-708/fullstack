    private void jButtonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualActionPerformed
        // TODO add your handling code here:
        if (acPress) {
            try {
                secondNum = Double.parseDouble(String.valueOf(jTextFieldDisplay.getText()));

                if (operator == 0) {
                    total = secondNum;
                    setText();
                }
                if (operator == 1) {
                    total = logic.add(firstNum, secondNum);
                    setText();
                } else if (operator == 2) {
                    total = logic.substract(firstNum, secondNum);
                    setText();
                } else if (operator == 3) {
                    total = logic.multiply(firstNum, secondNum);
                    setText();
                } else if (operator == 4) {
                    total = logic.divide(firstNum, secondNum);
                    setText();
                } else if (operator == 5) {
                    total = logic.percent(firstNum, secondNum);
                    setText();
                }
            } catch (Exception e) {
                jTextFieldDisplay.setText("Syntax Error");
                acPress = false;
            }
            firstNum = 0;
            secondNum = 0;
            operator = 0;
            total = 0;
            zeroDisp = false;
            decDisp = false;
        }
    }//GEN-LAST:event_jButtonEqualActionPerformed

