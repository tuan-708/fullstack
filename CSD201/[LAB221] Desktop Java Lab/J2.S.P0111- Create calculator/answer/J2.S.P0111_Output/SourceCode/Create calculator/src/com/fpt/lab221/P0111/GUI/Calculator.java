    public void addText(JButton btn) {
        if (acPress) {
            if (!zeroDisp && !decDisp) {
                jTextFieldDisplay.setText(null);
            }
            jTextFieldDisplay.setText(jTextFieldDisplay.getText() + btn.getText());
            zeroDisp = true;
            numberAdd = true;
        }
    }

    public void setText() {
        if (total > -100000000 && total < 1000000000) {
            BigDecimal bigDec = BigDecimal.valueOf(total);

            jTextFieldDisplay.setText(String.valueOf(bigDec.doubleValue()));
        } else {
            jTextFieldDisplay.setText("Math Error");
        }
    }

    public void prevTotal() {
        if (acPress) {
            if (operator == 0) {
                try {
                    firstNum = Double.parseDouble(String.valueOf(jTextFieldDisplay.getText()));
                } catch (Exception e) {
                    jTextFieldDisplay.setText("Syntax Error");
                    acPress = false;
                }
            } else {
                try {
                    secondNum = Double.parseDouble(String.valueOf(jTextFieldDisplay.getText()));
                } catch (Exception e) {
                    jTextFieldDisplay.setText("Syntax Error");
                    acPress = false;
                }
            }
            if (numberAdd) {
                if (operator == 1) {
                    firstNum = logic.add(firstNum, secondNum);
                } else if (operator == 2) {
                    firstNum = logic.substract(firstNum, secondNum);
                } else if (operator == 3) {
                    firstNum = logic.multiply(firstNum, secondNum);
                } else if (operator == 4) {
                    firstNum = logic.divide(firstNum, secondNum);
                }
            }
            jTextFieldDisplay.setText(String.valueOf(firstNum));
            zeroDisp = false;
            decDisp = false;
        }
    }

