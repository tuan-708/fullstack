    private void jButtonBackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackspaceActionPerformed
        // TODO add your handling code here:
        if (acPress) {
            int length;
            String str = jTextFieldDisplay.getText();
            length = str.length();
            if (length > 1) {
                str = str.substring(0, length - 1);
                jTextFieldDisplay.setText(str);
            } else if (length == 1) {
                jTextFieldDisplay.setText("0");
                zeroDisp = false;
                decDisp = false;
            }
        }
    }//GEN-LAST:event_jButtonBackspaceActionPerformed

